/**
 * Bundles the docs markdown (`docs/src/content/**\/*.md`) into one generated module
 * `docs/src/content/content.gen.ts` — a slug → source map the app imports.
 *
 * Why a codegen step (not a raw `.md` import): the Weave/esbuild build has no `.md`
 * loader, and markdown's backticks can't live in a hand-written `.ts` template
 * literal. JSON.stringify here escapes everything safely. Mirrors `routes.gen.ts`.
 *
 * Runs before `weave dev`/`build` (see the `docs` / `docs:build` npm scripts).
 */

import { readdir, readFile, writeFile } from 'node:fs/promises';
import { join, relative, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const contentDir = join(here, '..', 'src', 'content');
const outFile = join(contentDir, 'content.gen.ts');

/** Recursively collect every `.md` file under a directory. */
async function walk(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = [];
  for (const e of entries) {
    const full = join(dir, e.name);
    if (e.isDirectory()) files.push(...(await walk(full)));
    else if (e.name.endsWith('.md')) files.push(full);
  }
  return files;
}

const files = (await walk(contentDir)).sort();
const entries = [];
const perPage = [];
for (const file of files) {
  const slug = relative(contentDir, file).replace(/\\/g, '/').replace(/\.md$/, '');
  const src = await readFile(file, 'utf8');
  entries.push(`  ${JSON.stringify(slug)}: ${JSON.stringify(src)},`);

  // ONE module per page, next to its .md. A page imports only its own, so the bundler puts that page's
  // markdown in that page's route chunk — which is the whole point of splitting the routes at all.
  //
  // The map below used to be the only artifact, and EVERY page imported it to pick one key out. So every
  // page downloaded all 113 pages' markdown (~217 KB gz) and the route split bought almost nothing: measured,
  // a page went 427.6 → 397.6 KB gz, not the 36× a look at the file sizes on disk suggested. What a page
  // LOADS is not what its own chunk weighs.
  const pageFile = file.replace(/\.md$/, '.gen.ts');
  perPage.push(
    writeFile(
      pageFile,
      `// AUTO-GENERATED by docs/tools/gen-content.mjs — do not edit.\n` +
        `// The markdown source of ${slug}.md, as its own module so it lands in its own page's chunk.\n\n` +
        `export const source: string = ${JSON.stringify(src)};\n`,
      'utf8'
    )
  );
}
await Promise.all(perPage);

// The full map stays — `lib/search/build-index` needs every page at once to build the index. It is imported
// lazily now (on first search), so it is its own chunk and no page pays for it up front.
const out = `// AUTO-GENERATED by docs/tools/gen-content.mjs — do not edit.
// Maps a content slug (relative path under src/content, no extension) to its markdown source.
// Regenerated on every \`pnpm docs\` / \`pnpm docs:build\`.
//
// For the SEARCH INDEX only — it is the one consumer that legitimately needs every page at once, and it
// imports this lazily. A PAGE must import its own \`<slug>.gen.ts\` instead: importing this map to pick one
// key out drags all 113 pages into that page's download.

export const content: Record<string, string> = {
${entries.join('\n')}
};
`;

await writeFile(outFile, out, 'utf8');
console.log(
  `gen-content → ${relative(join(here, '..', '..'), outFile)} + ${files.length} per-page module${files.length === 1 ? '' : 's'}`
);
