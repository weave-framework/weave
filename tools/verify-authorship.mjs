/**
 * Guard: the public repository must not describe who or what authored it.
 *
 * Named for what it checks, not for the words it forbids — an earlier name contained one of its own
 * patterns and the gate flagged its own wiring. That is a small thing, but a gate whose first act is a
 * false positive on itself does not earn trust.
 *
 * A local `commit-msg` hook already refuses three vendor names in a commit message. That is narrower than
 * the rule it enforces, in two ways that were both reached in practice on 2026-07-19:
 *
 *  - it matches NAMES, not the claim — a message describing the policy itself passed cleanly, which put
 *    the very thing the rule excludes into the history;
 *  - it reads the commit MESSAGE only, so tracked source files were never checked, and a comment in
 *    `verify-no-private-paths.mjs` carried the same wording.
 *
 * It also lives in `.git/hooks`, which git neither versions nor clones, so it protects one machine.
 *
 * This runs in CI over tracked file CONTENT and over commit messages not yet on the remote, so the rule
 * travels with the repository. If it fires, the fix is to DELETE the sentence — not to spell the word
 * differently. Routing around a gate that is telling you something true is how the original slip happened.
 */
import { spawnSync } from 'node:child_process';
import { readFileSync } from 'node:fs';

/**
 * Wordings that claim authorship, not the bare word "AI".
 *
 * Weave has real AI-facing surfaces — an MCP server, editor integrations, the skills — and prose about
 * those is product documentation, not a trace of how the code was written. A gate that flags legitimate
 * subject matter gets switched off, which is worse than having no gate at all.
 */
const PATTERNS = [
  /\bAI[-\s]?(assisted|generated|authored|written)\b/i,
  /\bAI[-\s]traces?\b/i,
  /\b(written|authored|generated|produced)\s+(by|with)\s+(an?\s+)?(AI|assistant|model|agent)\b/i,
  /\bco-?authored-by:\s*(?!.*@users\.noreply\.github\.com)/i,
];

/**
 * DIVISION OF LABOUR, stated so the gap is documented rather than hidden.
 *
 * Specific vendor names are NOT listed here, and deliberately: a public file enumerating the names a
 * project refuses to mention is itself the disclosure it is trying to prevent — it tells a reader exactly
 * what is being kept out. The local `commit-msg` hook holds that list, outside the repository, where naming
 * them costs nothing.
 *
 * So: the hook catches names in commit messages; this catches authorship CLAIMS in file content and in
 * messages, which the hook never reads. A vendor name appearing in a tracked FILE is covered by neither,
 * and that is a known hole rather than an oversight — closing it would require publishing the list.
 *
 * The first attempt at this file did list them, and the hook refused the commit. That refusal was correct.
 * Splitting the literals to get past it — which is what the earlier `verify-no-private-paths.mjs` entry
 * did — is how the original slip happened; the answer is to restructure, not to respell.
 */

/** Files where these words are legitimate subject matter. */
const ALLOW = [
  /^tools\/verify-authorship\.mjs$/,
  /^docs\/src\/content\//,
  /^packages\/mcp\//,
  /^skills\//,
  /^packages\/create-weave\/template\//,
];

/** Not text — reading these yields mojibake that trips any pattern by chance. */
const BINARY = /\.(vsix|zip|png|jpg|jpeg|gif|ico|woff2?|ttf|eot|pdf|jar|gz|tgz|webp|avif|mp4|wasm)$/i;

const sh = (args) => {
  const r = spawnSync('git', args, { encoding: 'utf8' });
  if (r.status !== 0) {
    console.error('x git failed:', r.stderr);
    process.exit(1);
  }
  return r.stdout;
};

const scan = (label, text) => {
  let hits = 0;
  text.split(/\r?\n/).forEach((line, i) => {
    for (const re of PATTERNS) {
      if (re.test(line)) {
        console.error(`  ${label}:${i + 1}  ${line.trim().slice(0, 110)}`);
        hits++;
        break;
      }
    }
  });
  return hits;
};

console.log('\ntools/verify-authorship.mjs');

// SELF-CHECK. The first version of this file shipped with backspace characters where its escapes were meant
// to be, so every pattern was unmatchable and the gate passed on a repository that plainly failed it. A gate
// that cannot fail is not a gate — so prove the patterns bite before trusting a clean result.
const CANARY = 'this line was AI-generated by an assistant';
if (!PATTERNS.some((re) => re.test(CANARY))) {
  console.error('x the patterns do not match their own canary — this gate is not functioning');
  process.exit(1);
}

let failures = 0;

const tracked = sh(['ls-files']).split('\n').map((l) => l.trim()).filter(Boolean);
for (const file of tracked) {
  if (BINARY.test(file) || ALLOW.some((re) => re.test(file))) continue;
  let text;
  try {
    text = readFileSync(file, 'utf8');
  } catch {
    continue;
  }
  failures += scan(file, text);
}

// Commit messages not yet on the remote — still rewritable, and the last chance to catch one.
const range = spawnSync('git', ['rev-list', 'origin/main..HEAD'], { encoding: 'utf8' });
if (range.status === 0) {
  for (const sha of range.stdout.split('\n').map((l) => l.trim()).filter(Boolean)) {
    failures += scan(`commit ${sha.slice(0, 8)}`, sh(['log', '-1', '--format=%B', sha]));
  }
}

if (failures) {
  console.error(
    `\nx ${failures} line(s) describe how this repository is authored.\n` +
      '  Delete the sentence. Do not respell the word to satisfy the pattern — the gate is right.\n'
  );
  process.exit(1);
}
console.log(`ok  no authorship claims in ${tracked.length} tracked files or the unpushed commit messages\n`);
